#Recebendo informaçoes da base e da altura do triangulo
info = float(input("Digite aqui a base do seu triangulo:"))
info2 = float(input("Digite aqui a altura do seu triangulo:"))

#Operaçao matematica para descubrir a area do triangulo
area = (info*info2)/2

#Imprimindo area do triangulo
print(f"A area desse triangulo sera: {area}")